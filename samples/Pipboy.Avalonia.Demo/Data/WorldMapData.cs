    // ── Geometry builder ──────────────────────────────────────────────────────

    private static Geometry BuildPolygon((int lon, int lat)[] pts)
    {
        var geo = new StreamGeometry();
        using var ctx = geo.Open();

        // Avalonia Y axis is downward. Latitude is upward, so invert Y.
        // Storing Y = -lat means north (high lat) maps to smaller Y (top of screen).
        ctx.BeginFigure(new Point(pts[0].lon, -pts[0].lat), true);
        for (int i = 1; i < pts.Length; i++)
            ctx.LineTo(new Point(pts[i].lon, -pts[i].lat));
        ctx.EndFigure(true);

        return geo;
    }
